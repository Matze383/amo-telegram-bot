def normalize_lang(value: str | None) -> str:
    candidate = (value or "").strip().lower()
    return candidate if candidate in _ALLOWED_LANGS else _DEFAULT_LANG


def resolve_lang() -> str:
    query_lang = request.args.get("lang")
    if query_lang is not None:
        lang = normalize_lang(query_lang)
        session["lang"] = lang
        return lang

    stored_lang = session.get("lang")
    if isinstance(stored_lang, str) and stored_lang in _ALLOWED_LANGS:
        return stored_lang

    session["lang"] = _DEFAULT_LANG
    return _DEFAULT_LANG


def translate(key: str, *, lang: str | None = None) -> str:
    resolved = normalize_lang(lang) if lang is not None else resolve_lang()
    return _TRANSLATIONS.get(resolved, {}).get(key, _TRANSLATIONS[_DEFAULT_LANG].get(key, key))
